package myJava.concepts;

//To find sum of two numbers
import java.lang.*;

public class SumOfTwoNumbers {
	public static void main(String args[]) {
		int x, y, z;
		x = 10;
		y = 30;
		z = x + y;
		System.out.println("The sum of " + x + " and " + y + " is " + z);
	}
}
/*
 * Output The sum of 10 and 30 is 40
 */