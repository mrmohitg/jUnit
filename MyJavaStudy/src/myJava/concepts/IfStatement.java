package myJava.concepts;

//To use if statement
public class IfStatement {
	public static void main(String args[]) {
		int i = 3, j = 4;
		if (i < j) {
			System.out.println("The value of i i.e. " + i + " is less then j i.e. " + j + " .");
		}
		if (i > j) {
			System.out.println("The value of i i.e. " + i + " is greater then j i.e. " + j + " .");
		}
	}
}
/*
 * Output The value of i i.e. 3 is less then j i.e. 4 .
 */